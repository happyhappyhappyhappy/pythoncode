def solver(N:int,K:int,A:list):
    result = (K*(K+1))//2
    Data=set()
    for j in range(N):
        if A[j] <= K:
            Data.add(A[j])
    for _ in range(len(Data)):
        x = Data.pop()
        result = result - x
    return result

