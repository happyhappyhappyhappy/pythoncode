MAXSIZE = ( 1 << 59 ) -1
MINSIZE = -( 1 << 59) + 1

def solver():
    N=II()
    A=[]
    B=[]
    for _ in range(N):
        a,b=MI()
        A.append(a)
        B.append(b)
    xdebug(f"A={A}")
    xdebug(f"B={B}")
    return 1

def resolve():
    print(solver())

